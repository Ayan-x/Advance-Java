/*There  are three type of interface
->Normal Interface-This interface contain two or more method.
->Functional Interface- This interface contain only one method.
->Marker Interface- These are blank interface which is used serialization , clone object etc.

Serialization -> Converting a object into byte stream to store it in a file or dtatabase, to sent to other machine.
DeSerialization -> Converting byte stream to object . Used for object reconstruction including its field value.
Ex-> if we want to restore the game with its progress then it use Serialization to store and DeSerialization 
to resume the game from that point.

*/